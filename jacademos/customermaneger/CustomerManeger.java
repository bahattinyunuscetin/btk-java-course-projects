// Java'da Nesne Yönelimli Programlama (OOP) mantığını anlatan bir eğitim kodu

// Müşteri işlemlerini yöneten bir sınıf
class CustomerManager {

    // Müşteri ekleme işlemi
    public void add() {
        System.out.println("Müşteri eklendi.");
    }

    // Müşteri silme işlemi
    public void remove() {
        System.out.println("Müşteri silindi.");
    }

    // Müşteri güncelleme işlemi
    public void update() {
        System.out.println("Müşteri güncellendi.");
    }
}

